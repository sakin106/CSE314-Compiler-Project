static char pstk[MAXSTACK][MAXSYM];
static int  ptop = -1;

static void push(const char *str) {
    if (ptop >= MAXSTACK-1) { printf("Stack overflow\n"); exit(1); }
    strncpy(pstk[++ptop], str, MAXSYM-1); pstk[ptop][MAXSYM-1] = '\0';
}

static const char *stk_pop(void) { return ptop >= 0 ? pstk[ptop--] : NULL; }

static void snap(char *out, int len) {
    out[0]='['; out[1]='\0'; char tmp[4096]="";
    int k;
    for (k = ptop; k >= 0; k--){
        strncat(tmp, pstk[k], sizeof(tmp)-strlen(tmp)-1);
        if(k > 0) strncat(tmp, " ", sizeof(tmp)-strlen(tmp)-1);
    }
    strncat(out, tmp, (size_t)(len-(int)strlen(out)-3));
    strncat(out, "]", len-strlen(out)-1);
    if((int)strlen(out) > len-1){ out[len-4]='.'; out[len-3]='.'; out[len-2]='.'; out[len-1]='\0'; }
}

static void ptrace(const char *lk, const char *top, const char *prod) {
    char str[50]; snap(str, sizeof(str));
    printf("%-35s %-16s %-15s %s\n", str, lk, top, prod);
}

