typedef struct { char value[MAXSYM]; char type[MAXSYM]; } Token;
Token tokenStream[MAXTOK];
int   tokenCount  = 0;
int   lexHasError = 0;

void addToken(const char *val, const char *typ) {
    if (tokenCount >= MAXTOK - 1) return;
    strncpy(tokenStream[tokenCount].value, val, MAXSYM - 1);
    strncpy(tokenStream[tokenCount].type,  typ, MAXSYM - 1);
    tokenStream[tokenCount].value[MAXSYM-1] = '\0';
    tokenStream[tokenCount].type[MAXSYM-1]  = '\0';
    tokenCount++;
}

void lexicalAnalyzer(FILE *fp) {
    int  line  = 1;
    int  ch;

    init_dfa_cols();

    while ((ch = fgetc(fp)) != EOF) {
        if (ch == '\n') { line++; continue; }
        if (ch == ' ' || ch == '\t') continue;

        char buf[MAXSYM];
        int  k     = 0;
        int  state = UDFA_START;
        int  next;

        int col = dfa_col[(unsigned char)ch];
        next = udfa_next[state][col];
        if (next == -1 || next == UDFA_DEAD) {
            printf("LEXICAL ERROR line %d: Unknown character '%c'\n", line, ch);
            lexHasError = 1;
            continue;
        }

        buf[k++] = (char)ch;
        state = next;

        while (k < MAXSYM - 1) {
            ch = fgetc(fp);
            if (ch == EOF) break;

            col  = dfa_col[(unsigned char)ch];
            next = udfa_next[state][col];

            if (next == -1 || next == UDFA_DEAD) {
                ungetc(ch, fp);
                break;
            }
            buf[k++] = (char)ch;
            state = next;
        }
        buf[k] = '\0';

        int tok = dfa_token_type[state];

        if (tok == 0) {
            printf("LEXICAL ERROR line %d: Unrecognised sequence '%s'\n", line, buf);
            lexHasError = 1;
            continue;
        }

        /* Comment validation: body may only contain spaces and alphabets */
        if (tok == 14) {
            int valid = 1;
            int s;
            for (s = 2; s < k - 1; s++) {
                unsigned char bc = (unsigned char)buf[s];
                int bc_col = dfa_col[bc];
                if (!( (bc_col >= 1 && bc_col <= 24) || bc_col == 40 )) {
                    valid = 0; break;
                }
            }
            if (!valid) {
                printf("LEXICAL ERROR line %d: Comment may only contain spaces and alphabets\n", line);
                lexHasError = 1;
            }
            line++;
            continue;
        }

        if (tok == 1 && line != 1) {
            printf("LEXICAL ERROR line %d: #include only allowed on line 1\n", line);
            lexHasError = 1;
            continue;
        }

        addToken(buf, TOKEN_TYPE_NAME[tok]);
    }

    addToken("$", "$");
}

